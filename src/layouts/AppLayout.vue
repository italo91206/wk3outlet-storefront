<template>
  <component :is="layout">
    <slot />
  </component>
</template>

<script>
const defaultLayout = "AppLayoutDefault";

export default {
  name: "AppLayout",
  computed: {
    layout() {
      const name = this.$route.meta.layout || "None";
      const layout = name || defaultLayout;
      return () => import(`@/layouts/${layout}.vue`);
    },
  },
};
</script>
